def area( b1, b2, h ):
    return ((b1 + b2) * h) / 2

print( "{0:.1f}".format(  area( 3, 3, 3 )  ) )
print( "{0:.1f}".format(  area( 5, 6, 7 )  ) )
print( "{0:.1f}".format(  area( 7, 10, 6 )  ) )
print( "{0:.1f}".format(  area( 13, 9, 3 )  ) )
print( "{0:.1f}".format(  area( 6, 11, 4 )  ) )
